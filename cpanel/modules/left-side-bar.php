<script>
    var activeMenu = {
        init: function () {
            var path = location.href.split('?')[0];
            $('#sidebar ul.sidebar-menu>li>ul>li>a').each(function (key, elem) {
                var href = $(elem).attr('href').split('?')[0];
                if (path.indexOf(href) >= 0) {
                    $(elem).parents('li:first').addClass('active')
                        .parents('li:first').addClass('open active');
                    //$(elem).parents('li:eq(1)',).addClass('open active');
                }
            });
        }
    };
    activeMenu.init();
</script>